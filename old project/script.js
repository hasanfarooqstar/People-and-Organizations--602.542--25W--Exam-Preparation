  score = 0;
  answeredCount = 0;
  showAnswersMode = false;
  showAnswersButton.textContent = "Show All Correct Answers";

  let questions;
  if (isMaster) {
    questions = generateMasterExamData();
  } else {
    // Identify current lecture again or pass it in. 
    // Simplest: re-run logic or just grab from current DOM? No, DOM is dirty.
    // We need original data.
    let lectureKey = "lecture1";
    for (let i = 1; i <= 8; i++) {
      if (window.location.href.includes(`lecture${i}`)) {
        lectureKey = `lecture${i}`;
        break;
      }
    }
    questions = quizData[lectureKey];
  }

  renderQuiz(questions);

  // We need to update the click listener for Show Answers to point to the NEW questions?
  // Accessing `questions` in `toggleShowAnswers` via closure within `init` works, 
