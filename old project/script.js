function init() {
  if (!quizContainer) return; // We are on index page probably

  let questions = [];
  let isMaster = false;

  // Check for Master Exam
  if (window.location.href.includes('master-exam')) {
    isMaster = true;
    questions = generateMasterExamData();
  } else {
    // Determine lecture key
    let lectureKey = "lecture1";
    for (let i = 1; i <= 8; i++) {
      if (window.location.href.includes(`lecture${i}`)) {
        lectureKey = `lecture${i}`;
        break;
      }
    }
    questions = quizData[lectureKey];
  }

  if (!questions || questions.length === 0) {
    quizContainer.innerHTML = "<p>Error: No data found.</p>";
    return;
  }

  // Set Global
  currentQuestions = questions;

  renderQuiz(currentQuestions);
  updateScore();

  resetButton.addEventListener('click', () => {
    if (confirm(isMaster ? "Are you sure you want to reset and generate a NEW exam?" : "Are you sure you want to reset the quiz?")) {
      resetQuiz(isMaster);
    }
  });

  showAnswersButton.addEventListener('click', () => {
    toggleShowAnswers(currentQuestions);
  });
}


function generateMasterExamData() {
  // 1. Flatten all lectures
  let allQuestions = [];
  Object.keys(quizData).forEach(key => {
    allQuestions = allQuestions.concat(quizData[key]);
  });

  // 2. Shuffle array
  for (let i = allQuestions.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [allQuestions[i], allQuestions[j]] = [allQuestions[j], allQuestions[i]];
  }

  // 3. Take first 40
  // Re-assign IDs for the view (1 to 40) but keep original info probably?
  // The render function uses ID for DOM mapping `q-${q.id}`.
  // If we have duplicate IDs from different lectures (e.g. Q1 from Lec1 and Q1 from Lec2), we might have issues if we don't unique-ify them.
  // However, `renderQuiz` creates DOM elements. `handleAnswer` refers to `q.id`.
  // If two questions have ID=1, `document.getElementById('q-1')` will return the first one.
  // So we MUST re-map IDs for the current session.

  const selected = allQuestions.slice(0, 40).map((q, index) => {
    // Create a shallow copy to avoid mutating the original reference in quizData
    return {
      ...q,
      id: index + 1, // New sequence 1-40
      originalId: q.id, // Keep verify info if needed
      originalLecture: q.type // or infer from source
    };
  });

  return selected;
}

function renderQuiz(questions) {
  quizContainer.innerHTML = "";
  questions.forEach((q, index) => {
    const card = document.createElement('div');
    card.className = "mcq-card";
    // Ensure ID is unique in DOM. renderQuiz uses q.id.
    // For Master Exam, we re-mapped q.id to 1..40.
    // For Lecture Exam, q.id is 1..40 from file.
    card.id = `q-${q.id}`;

    const questionTitle = document.createElement('div');
    questionTitle.className = "question-text";
    questionTitle.textContent = `${q.id}. ${q.question}`;
    card.appendChild(questionTitle);

    const optionsList = document.createElement('div');
    optionsList.className = "options-list";

    const keys = ['a', 'b', 'c', 'd'];
    keys.forEach(key => {
      if (!q.options[key]) return;

      const btn = document.createElement('div');
      btn.className = "option";
      btn.dataset.key = key;
      btn.dataset.qid = q.id;
      btn.textContent = `${key.toUpperCase()}) ${q.options[key]}`;

      btn.addEventListener('click', () => handleAnswer(q, key, btn, optionsList));
      optionsList.appendChild(btn);
    });

    card.appendChild(optionsList);

    const meta = document.createElement('div');
    meta.className = "meta-info";
    meta.innerHTML = `
            <span>Type: ${q.type}</span>
            <span class="badge">Slide: ${q.slide}</span>
        `;
    card.appendChild(meta);

    quizContainer.appendChild(card);
  });
}

function handleAnswer(question, selectedKey, btnElement, optionsList) {
  if (btnElement.parentElement.classList.contains('answered')) return;

  optionsList.classList.add('answered');
  Array.from(optionsList.children).forEach(child => child.classList.add('disabled'));

  const isCorrect = selectedKey === question.answer;

  if (isCorrect) {
    btnElement.classList.add('correct');
    btnElement.textContent += "  Correct";
    score++;
  } else {
    btnElement.classList.add('wrong');
    btnElement.textContent += " ";

    const correctBtn = optionsList.querySelector(`[data-key="${question.answer}"]`);
    if (correctBtn) {
      correctBtn.classList.add('correct');
      correctBtn.textContent += " ";
    }
  }

  answeredCount++;
  updateScore();
}

function updateScore() {
  const total = document.querySelectorAll('.mcq-card').length || 40;
  scoreDisplay.textContent = `Score: ${score} / ${total}`;
}

